import Foundation

extension UIScreen {

    /// UIScreen.main.bounds.width 的简便方法
    class var mainWidth: CGFloat {
        return UIScreen.main.bounds.width
    }

    /// UIScreen.main.bounds.height 的简便方法
    class var mainHeight: CGFloat {
        return UIScreen.main.bounds.height
    }
}
